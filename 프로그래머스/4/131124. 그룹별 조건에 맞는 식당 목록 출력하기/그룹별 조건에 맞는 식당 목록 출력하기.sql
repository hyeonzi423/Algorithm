WITH MAX_REVIEW_CNT AS (
    SELECT COUNT(*)
    FROM REST_REVIEW 
    GROUP BY MEMBER_ID
    ORDER BY 1 DESC
    FETCH FIRST 1 ROWS ONLY
), MAX_REVIEW_MEMBERS AS (
    SELECT MEMBER_ID
    FROM REST_REVIEW 
    GROUP BY MEMBER_ID
    HAVING COUNT(*) = (
        SELECT * FROM MAX_REVIEW_CNT
    )
)

SELECT M.MEMBER_NAME, R.REVIEW_TEXT, TO_CHAR(REVIEW_DATE, 'YYYY-MM-DD') REVIEW_DATE
FROM MEMBER_PROFILE M JOIN REST_REVIEW R
     ON M.MEMBER_ID = R.MEMBER_ID
WHERE M.MEMBER_ID IN (SELECT * FROM MAX_REVIEW_MEMBERS)
ORDER BY 3, 2;
